
package Doituong;


public class Phong {
    int maphong;
    String tenphong;
    int matoanha;
    int manguoithue;
    int gia;
    int soluongnguoi;
    int soluongxe;
    int dien;
    int nuoc;
    String thanhtoan;

    public Phong() {
    }

    public Phong(int maphong, String tenphong, int matoanha, int manguoithue, int gia, int soluongnguoi, int soluongxe, int dien, int nuoc, String thanhtoan) {
        this.maphong = maphong;
        this.tenphong = tenphong;
        this.matoanha = matoanha;
        this.manguoithue = manguoithue;
        this.gia = gia;
        this.soluongnguoi = soluongnguoi;
        this.soluongxe = soluongxe;
        this.dien = dien;
        this.nuoc = nuoc;
        this.thanhtoan = thanhtoan;
    }

    public int getMaphong() {
        return maphong;
    }

    public void setMaphong(int maphong) {
        this.maphong = maphong;
    }

    public String getTenphong() {
        return tenphong;
    }

    public void setTenphong(String tenphong) {
        this.tenphong = tenphong;
    }

    public int getMatoanha() {
        return matoanha;
    }

    public void setMatoanha(int matoanha) {
        this.matoanha = matoanha;
    }

    public int getManguoithue() {
        return manguoithue;
    }

    public void setManguoithue(int manguoithue) {
        this.manguoithue = manguoithue;
    }

    public int getGia() {
        return gia;
    }

    public void setGia(int gia) {
        this.gia = gia;
    }

    public int getSoluongnguoi() {
        return soluongnguoi;
    }

    public void setSoluongnguoi(int soluongnguoi) {
        this.soluongnguoi = soluongnguoi;
    }

    public int getSoluongxe() {
        return soluongxe;
    }

    public void setSoluongxe(int soluongxe) {
        this.soluongxe = soluongxe;
    }

    public int getDien() {
        return dien;
    }

    public void setDien(int dien) {
        this.dien = dien;
    }

    public int getNuoc() {
        return nuoc;
    }

    public void setNuoc(int nuoc) {
        this.nuoc = nuoc;
    }

    public String getThanhtoan() {
        return thanhtoan;
    }

    public void setThanhtoan(String thanhtoan) {
        this.thanhtoan = thanhtoan;
    }
    
}
