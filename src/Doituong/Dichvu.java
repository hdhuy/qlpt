
package Doituong;


public class Dichvu {
    int dien;
    int nuoc;

    public Dichvu() {
    }

    public Dichvu(int dien, int nuoc) {
        this.dien = dien;
        this.nuoc = nuoc;
    }

    public int getDien() {
        return dien;
    }

    public void setDien(int dien) {
        this.dien = dien;
    }

    public int getNuoc() {
        return nuoc;
    }

    public void setNuoc(int nuoc) {
        this.nuoc = nuoc;
    }
    
}
