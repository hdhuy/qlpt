package Doituong;

/**
 *
 * @author Huy
 */
public class Dangnhap {
    String ten;
    String mk;

    public Dangnhap() {
    }

    public Dangnhap(String ten, String mk) {
        this.ten = ten;
        this.mk = mk;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMk() {
        return mk;
    }

    public void setMk(String mk) {
        this.mk = mk;
    }
    
    
}
