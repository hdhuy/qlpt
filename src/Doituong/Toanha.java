
package Doituong;

public class Toanha {
    int matoanha;
    String tentoanha;
    String diachi;

    public Toanha() {
    }

    public Toanha(int matoanha, String tentoanha, String diachi) {
        this.matoanha = matoanha;
        this.tentoanha = tentoanha;
        this.diachi = diachi;
    }

    public int getMatoanha() {
        return matoanha;
    }

    public void setMatoanha(int matoanha) {
        this.matoanha = matoanha;
    }

    public String getTentoanha() {
        return tentoanha;
    }

    public void setTentoanha(String tentoanha) {
        this.tentoanha = tentoanha;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    
}
