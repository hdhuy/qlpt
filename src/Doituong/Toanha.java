
package Doituong;

public class Toanha {
    int matoanha;
    String tentoanha;
    String diachi;
    int sophong;

    public Toanha() {
    }

    public Toanha(int matoanha, String tentoanha, String diachi, int sophong) {
        this.matoanha = matoanha;
        this.tentoanha = tentoanha;
        this.diachi = diachi;
        this.sophong = sophong;
    }

    public int getMatoanha() {
        return matoanha;
    }

    public void setMatoanha(int matoanha) {
        this.matoanha = matoanha;
    }

    public String getTentoanha() {
        return tentoanha;
    }

    public void setTentoanha(String tentoanha) {
        this.tentoanha = tentoanha;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public int getSophong() {
        return sophong;
    }

    public void setSophong(int sophong) {
        this.sophong = sophong;
    }
    
            
}
